/**
 * Copies Bootstrap dist assets from node_modules into the Jahia resource directories
 * and generates a JSP fragment exposing the Bootstrap version as an EL variable.
 *
 * Called automatically by npm via the "postinstall" script after `npm install`.
 * The Maven build (frontend-maven-plugin) triggers `npm install`, which in turn
 * runs this script so the files are present before the OSGi bundle is assembled.
 *
 * Source:      node_modules/bootstrap/dist/css/  ->  src/main/resources/css/
 *              node_modules/bootstrap/dist/js/   ->  src/main/resources/javascript/
 * Generated:   src/main/resources/bootstrap5nt_version/html/_bootstrap-version.jspf
 *              (sets ${bootstrapVersion} to the actual installed Bootstrap version)
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const root   = __dirname;
const srcCss = path.join(root, 'node_modules', 'bootstrap', 'dist', 'css');
const srcJs  = path.join(root, 'node_modules', 'bootstrap', 'dist', 'js');
const dstCss = path.join(root, 'src', 'main', 'resources', 'css');
const dstJs  = path.join(root, 'src', 'main', 'resources', 'javascript');

function copyDir(src, dst) {
    fs.mkdirSync(dst, { recursive: true });
    const files = fs.readdirSync(src);
    for (const file of files) {
        fs.copyFileSync(path.join(src, file), path.join(dst, file));
    }
    console.log(`[copy-bootstrap] ${files.length} files  ${src} -> ${dst}`);
}

copyDir(srcCss, dstCss);
copyDir(srcJs,  dstJs);

// Read the actual installed Bootstrap version and generate a JSP fragment so
// version.jsp never needs to hardcode the version string.
const bootstrapVersion = require('./node_modules/bootstrap/package.json').version;
const versionFragment  = path.join(root, 'src', 'main', 'resources', 'bootstrap5nt_version', 'html', '_bootstrap-version.jspf');
fs.writeFileSync(versionFragment,
    `<%-- Auto-generated by copy-bootstrap.js — do not edit manually --%>\n` +
    `<%@ taglib prefix="c" uri="http://java.sun.com/jsp/jstl/core" %>\n` +
    `<c:set var="bootstrapVersion" value="${bootstrapVersion}"/>\n`
);
console.log(`[copy-bootstrap] Bootstrap version ${bootstrapVersion} -> ${versionFragment}`);
